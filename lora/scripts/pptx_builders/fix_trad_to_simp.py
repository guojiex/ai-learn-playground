def fix_runs(shape, mapping):
    """Replace Traditional Chinese in runs with Simplified."""
    count = 0
    if shape.has_text_frame:
        for para in shape.text_frame.paragraphs:
            for run in para.runs:
                for trad, simp in mapping.items():
                    if trad in run.text:
                        run.text = run.text.replace(trad, simp)
                        count += 1
    if shape.has_table:
        for row in shape.table.rows:
            for cell in row.cells:
                for para in cell.text_frame.paragraphs:
                    for run in para.runs:
                        for trad, simp in mapping.items():
                            if trad in run.text:
                                run.text = run.text.replace(trad, simp)
                                count += 1
    return count


